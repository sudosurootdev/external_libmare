#pragma once

namespace mare {

namespace internal {

template<typename Item>
class storage_interface {
public:
  typedef Item item_t;

  virtual ~storage_interface() {}
  void enqueue(item_t* item);
  item_t* dequeue();                    /// blocking dequeue
  bool dequeue(item_t*& item);          /// non-blocking dequeue
  void wait();
  bool empty() const;
};

}; // namespace internal

}; // namespace mare
